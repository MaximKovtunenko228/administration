#!/bin/bash

# Функция для вывода таблицы всех планет с количеством спутников
print_all_planets() {
    printf "+------------+---------------------+\n"
    printf "| Планета    | Кол-во спутников    |\n"
    printf "+------------+---------------------+\n"
    printf "| Меркурий   | 0                   |\n"
    printf "| Венера     | 0                   |\n"
    printf "| Земля      | 1                   |\n"
    printf "| Марс       | 2                   |\n"
    printf "| Юпитер     | 97                  |\n"
    printf "| Сатурн     | 274                 |\n"
    printf "| Уран       | 28                  |\n"
    printf "| Нептун     | 16                  |\n"
    printf "| Плутон     | 5                   |\n"
    printf "+------------+---------------------+\n"
}

# Функция для вывода списка вариантов ввода
print_options() {
    echo "Доступные варианты для ввода:"
    echo "  Меркурий"
    echo "  Венера"
    echo "  Земля"
    echo "  Марс"
    echo "  Юпитер"
    echo "  Сатурн"
    echo "  Уран"
    echo "  Нептун"
    echo "  Плутон"
    echo "  Все  - вывести список всех планет"
    echo
}

# Если аргументов нет, выводим подсказку и список вариантов
if [ "$#" -ne 1 ]; then
    echo "Ошибка: необходимо указать название планеты или 'все'."
    print_options
    echo "Использование: $0 <название_планеты_на_русском> или '$0 все'"
    exit 1
fi

print_options

planet=$(echo "$1" | awk '{print tolower($0)}')

case "$planet" in
    "меркурий")
        moons=0
        ;;
    "венера")
        moons=0
        ;;
    "земля")
        moons=1
        ;;
    "марс")
        moons=2
        ;;
    "юпитер")
        moons=97
        ;;
    "сатурн")
        moons=274
        ;;
    "уран")
        moons=28
        ;;
    "нептун")
        moons=16
        ;;
    "плутон")
        moons=5
        ;;
    "все")
        print_all_planets
        exit 0
        ;;
    *)
        echo "Ошибка: планета '$1' не найдена в Солнечной системе."
        exit 1
        ;;
esac

if [ "$planet" = "плутон" ]; then
    echo "Планета '$1' имеет $moons спутник(а/ов)."
    echo "Кстати, Плутон — карликовая планета, но для нас он всё равно важен!"
else
    echo "Планета '$1' имеет $moons спутник(а/ов)."
fi
